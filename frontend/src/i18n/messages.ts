function getNestedMessage(source: Record<string, unknown>, path: string) {
  return path.split(".").reduce<unknown>((current, segment) => {
    if (current && typeof current === "object" && segment in current) {
      return (current as Record<string, unknown>)[segment];
    }
    return undefined;
  }, source);
}

export function resolveLocale(value: string | null | undefined): Locale {
  return value === "en" ? "en" : "vi";
}

export function getLocaleCode(locale: Locale) {
  return locale === "vi" ? "vi-VN" : "en-US";
}

export function getMessage(locale: Locale, path: string) {
  const localized = getNestedMessage(locale === "vi" ? (vi as Record<string, unknown>) : (en as Record<string, unknown>), path);
  if (typeof localized === "string") {
    return localized;
  }

  const fallback = getNestedMessage(en as Record<string, unknown>, path);
  return typeof fallback === "string" ? fallback : path;
}

export function getStatusLabel(locale: Locale, status: "Draft" | "Active" | "Completed") {
  switch (status) {
    case "Active":
      return getMessage(locale, "statuses.active");
    case "Completed":
      return getMessage(locale, "statuses.completed");
    case "Draft":
    default:
      return getMessage(locale, "statuses.draft");
  }
}

